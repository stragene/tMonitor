/***************************************************************
* 函数名称: vNBHal_Init
* 功能描述: 硬件中间层初始化，所调用函数为CobeMx自动生成
* 输入参数: 
* 输出参数: 
* 返 回 值: 
****************************************************************/
void vNBHal_Init(void)
{
    UART_NB.handler = USART1;
    memset(UART_NB.Sndbuf.data, 0, BUF_SIZE);
    UART_NB.Sndbuf.wr = 0;
    UART_NB.Sndbuf.rd = 0;
    memset(UART_NB.Rsvbuf.data, 0, BUF_SIZE);
    UART_NB.Rsvbuf.wr = 0;
    UART_NB.Rsvbuf.rd = 0;
}

